package com.class2;

public class VariablesDeclaration {
	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variables and assign value
		int num = 28;

		// 2. ways: using 2 steps
		int y; // 1.declaring variable
		y = 12; // 2. assign value

		// declare variables 1 by 1
		int a;
		int b;
		int c;
		int d;

		a = 10;
		b = 20;
		c = 32;
		d = 71;

		// or declare variable all together
		int x, f, g;

		x = 10;
		f = 32;
		g = 71;

		// for mav=c cdm+shift+f = formating
		// windows ctrl+shift+f = formating

	}

}
