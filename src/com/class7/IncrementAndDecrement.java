package com.class7;

public class IncrementAndDecrement {
public static void main(String[] args) {
	//post increment 

	// a++;               
	
	int a=299;
	int b=a++;
	System.out.println(a);
	System.out.println(b);
	
	
	//post decrement
	// a--;
	
	int z=10;
	z--;
	int c=z--;
	System.out.println(z);
	System.out.println(c);
	
		
	
}  }
